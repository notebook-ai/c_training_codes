int main()
{
    int num  = 15;
    float flt = 8.77;
    char character = 'R';
    char text[] = "welcome";

    printf("integer is: %d\n", num);
    printf("float is: %f\n", flt);
    printf("integer is: %d and float is: %f\n", num, flt);
    printf("character is: %c\n", character);
    printf("text is: %s\n", text);
    printf("index 3 of text: %c\n", text[3]);

    printf("\n-- Print Values Without Variables --\n");

    printf("My favorite number is: %d\n", 15);
    printf("My favorite letter is: %c\n", 'D');


}
