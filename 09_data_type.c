int main()
{
    int num = 32;
    printf("num is: %d\n", num);
    printf("num is: %i\n", num);

    float avg = 12.6;
    printf("1. avg is: %f\n", avg);
    printf("2. avg is: %.2f\n", avg);

    double dbl_num = 19.99;
    printf("double number is: %lf\n",dbl_num);

    char myLetter = 'D'; // Character
    printf("%c\n", myLetter);

    char greetings[] = "Hello World!";
    printf("%s\n", greetings);

    char A = 65, B = 66, C = 67, a = 97;
    printf("%c\n", A);
    printf("%c\n", B);
    printf("%c\n", C);
    printf("%c\n", a);




}
