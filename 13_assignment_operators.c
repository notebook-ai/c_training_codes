int main()
{
    int a = 10;
    a += 5; // a = a + 5;
    a -= 3; // a = a - 3;
    a *= 2; // a = a * 2;
    a /= 5; // a = a /5;
    a %= 4; // a = a % 4;

    int x = 6;
    x &= 3; // x = x & 3;
    x |= 2; // x = x | 2;
    x ^= 3; // x = x ^ 3;
    x >>= 3;  // x = x >> 3
    x <<= 3; // x = x << 3

}
