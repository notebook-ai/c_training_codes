#include <stdio.h>

int main(){
	int num = 45;
	float avg = 17.83;
	char character =  'n';
	char word[] = "smile";
	double price = 15.51;
	
	printf("int num: %d\n", num);
	printf("float avg: %.2f\n", avg);
	printf("char character: %c\n", character);
	printf("char word: %s\n", word);
	printf("double price: %.2lf\n", price);
	
	return 0;
}
